%==========================================================================
%-------The following function corresponds to the rational terms-----------
%-------in the reaction rates of the original reaction network-------------
%==========================================================================
function [d] = Denominator(x)
%--------------------------------------------------------------------------
%-------It is a function of one variables x, which is the vector of--------
%-------species' concentrations--------------------------------------------
%--------------------------------------------------------------------------
        d(1) = 1/(x(1) + (1+x(4))*(1+x(5)));   
        d(2) = 1/(x(1) + (1+x(4))*(1+x(5)));
        d(3) = 1;                              
        d(4) = 1;
        d(5) = 1;                              
        d(6) = 1;
        d(7) = 1;                              
        d(8) = 1;
        d(9) = 1;                              
        d(10) = 1;
        d(11) = 1;                             
        d(12) = 1;
        d(13) = 1;                             
        d(14) = 1;
        d(15) = x(16)/(1 + x(17) + x(18) + x(19) + x(18)*x(19));
        d(16) = x(16)/(1 + x(17) + x(18) + x(19) + x(18)*x(19));
        d(17) = 1/(1 + x(19));                 
        d(18) = 1/(1 + x(19));
        d(19) = 1;                             
        d(20) = 1;
end
